package edu.brown.cs.lsim2.frontend;

public class Answer {
	private static String cuisine; 
	private static double price; 
	private static int rating; 
	private static double time; 
	private static double distance; 
	private static String misc; 
	
	public Answer(String cuisine, double price, int rating, double time, double distance, String misc) {
		this.cuisine = cuisine; 
		this.price = price; 
		this.rating = rating; 
		this.time = time; 
		this.distance = distance; 
		this.misc = misc; 
	}
	
}
